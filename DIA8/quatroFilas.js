//Filas e mais filas

/*
1) Fila (Queue) – FIFO (First In, First Out)
    O primeiro elemento a entrar é o primeiro a sair.
    Exemplo: fila de banco.
    Operações principais: enqueue (inserir no final), dequeue (remover do início).

2) Fila Duplamente Encadeada (Deque - Double Ended Queue)
    Permite inserções e remoções tanto no início quanto no final da fila.
    Pode funcionar como fila ou pilha.
    Muito usada em algoritmos de janela deslizante.

3) Fila Circular (Circular Queue)
    A última posição é ligada à primeira, formando um "círculo".
    Evita desperdício de espaço em implementações com array fixo.
    Ideal para buffers circulares.

4) Fila de Prioridade (Priority Queue)
    Cada elemento tem uma prioridade, e o que tem maior prioridade sai primeiro.
    Pode ser implementada com heap (geralmente min-heap ou max-heap).
    Usada em algoritmos como Dijkstra.
*/

